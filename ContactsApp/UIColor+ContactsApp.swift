import Foundation
import UIKit

extension UIColor {
    
    static func fusionGreen() -> UIColor {
        return self.init(colorLiteralRed: 9.0/255.0, green: 207.0/255.0, blue: 192.0/255.0, alpha: 1.0)
    }
}
